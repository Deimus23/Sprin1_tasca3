package Nivel2.Tasca1;

public class Restaurant {
    private String name;
    private double score;

    public Restaurant(String name, double score) {
        this.name = name;
        this.score = score;
    }

    public double getScore() {
        return score;
    }

    public void setScore(double score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }


}
